package activites;

public class myName {

	public static void main(String[] args) 
	{
		//declare my name
		String myName = "Matthew";
		
		//use as a counter
		int number  = 0;
		
		//count up to 6
		for (number = 0; number < 6; number++)
		{
			//displays my name 6 times
			System.out.println(myName);
		}

	}

}