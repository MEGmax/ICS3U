package activites;

public class oddNumbers {

	public static void main(String[] args) 

	{
		//declare number as 1 as the start
		int number = 1;
		
		//header
		System.out.println("The odd numbers from 1-49 are: ");
		
		//count by 2 starting from one to get all odd numbers
		for (number = 1; number <=49; number+=2)
		{
			System.out.println(number);
		}
	
		
		
		
		

	}

}
